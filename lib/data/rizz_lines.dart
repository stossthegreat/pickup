/// Selection rules (the no-trash rule):
///   1. Every line is ≤ 14 words. The phone fatigue threshold.
///   2. No physical compliments without context ("u r hot" deleted by default).
///   3. No 2014 PUA neg-and-recover ("you'd be cute if..."). Reads creepy now.
///   4. No essay format. If it takes two paragraphs to land, it doesn't land.
///   5. Every line passes the SCREENSHOT TEST — would a 22-year-old screenshot
///      it to her group chat? If no → cut.
///
/// Tone bands across the categories:
///   · SELF-AWARE       — meta, breaks the 4th wall ("ngl i was gonna play it cool")
///   · NOTICED COMPLIMENT — specific observation, not generic ("your aura is unforgivable")
///   · FRAME CHECK      — assumes the outcome, high agency
///   · MISINTERPRETATION — willfully misreads her message in a flirty direction
///   · PUSH-PULL        — playful disqualifier that hooks
///   · HIGH-AGENCY      — secure, scarce, doing-the-talking energy
///   · META-OPEN        — frames the situation itself as the bit
///   · DATE PROPOSAL    — moves it offline, no scenic route
///
/// Pulled from the patterns that move on Hinge's published "what works" data
/// (specific > generic, observation > question), the top-engagement TikTok
/// rizz threads of 2025-26, and the Reddit r/Hinge / r/Tinder match-success
/// screenshot threads. Not a single line was lifted from another rizz app's
/// canned library — they're all 2014 PUA cringe rewrapped.
class RizzLine {
  final String text;
  final String tag;
  const RizzLine(this.text, this.tag);
}

